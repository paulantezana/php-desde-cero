<?php

/*
 * Operadores artiméticos:
 * + Suma
 * - Resta
 * * Multiplicación
 * / División
 * % Módulo
 */
echo 3 + 3;

echo 4 + 3 * 2 / 1;

/*
 * Prioridad de los operadores aritméticos
 * Multiplicación
 * División
 * Módulo
 * Suma
 * Resta
 */

/*
 * Buena práctica: Agrupar las operaciones matemáticas para evitar confusiones
 */
echo (4 + 3) * (2 / 1);